<?php

namespace App\Http\Controllers;

use App\Http\Controllers\Controller;
use App\Models\Contact;
use Illuminate\Support\Facades\DB;
use Illuminate\Pagination\CursorPaginator;
use Illuminate\Pagination\Cursor;
use Illuminate\Http\Request;

class ContactController extends Controller {

  public function result() {

    return view('contact.result', ['contacts' => DB::table('contacts')->paginate(15)]);
  }

  public function form(Request $request) {

    //dump($request);
    if ($request->has('newName') AND $request->has('newPhone')) {

      $validated = $request->validate([
          'newName' => 'string',
          'newPhone' => 'numeric',
      ]);
      //dd($validated);

      $newContact = new Contact;
      $newContact->name = $validated['newName'];
      $newContact->phone = $validated['newPhone'];
      $newContact->save();
      return view('contact.sucsessful');
    } else {
      return view('contact.form');
    }
  }
  
  public function delete(Request $request) {

    if ($request->has('id')) {

      $validated = $request->validate([
          'id' => 'numeric',
      ]);

      $contact = Contact::find($validated['id']);
      $contact->delete();
      return view('contact.sucsessful');
    } 
  }
  
  public function edit(Request $request) {

    if ($request->has('id')) {

      $validated = $request->validate([
          'id' => 'numeric',
      ]);

      $contact = Contact::find($validated['id']);

      return view('contact.editform', ['contact' => $contact]);
    }
    
    if ($request->has('editName') AND $request->has('editPhone') AND $request->has('editId')) {

      $validated = $request->validate([
          'editName' => 'string',
          'editPhone' => 'numeric',
          'editId' => 'numeric',
      ]);

      $editContact = Contact::find($validated['editId']);
      $editContact->name = $validated['editName'];
      $editContact->phone = $validated['editPhone'];
      $editContact->save();
      return view('contact.sucsessful');
      
    }
    
   
    
  }

}

